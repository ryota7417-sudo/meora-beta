// 危機ワード検知とアプリ側ガードレール。
// モデルには相談窓口の電話番号・名称を生成させない方針のため、
// ユーザー発話に危機ワードが含まれた場合は、アプリ側で確定文として
// 「正規の相談窓口」案内を付加する。番号・窓口名はすべて下記の固定値を使う。

// 危機ワードのリスト（部分一致・ひらがな/漢字混在を想定）。
// 将来追加・削除しやすいよう一箇所に集約する。
export const CRISIS_WORDS: string[] = [
  '死にたい',
  '消えたい',
  'いなくなりたい',
  '自殺',
  '自傷',
  'リストカット',
  'リスカ',
  'もう生きていけない',
  '死のう',
  '飛び降り',
  '過量服薬',
  'オーバードーズ',
  'OD',
];

// 危機ワードを含むかどうかを判定する。
// テキストを正規化（前後空白除去）してから部分一致でチェックする。
// 「OD」のみ大文字小文字を区別せず判定する（od / Od などの表記ゆれ対策）。
export function detectCrisis(text: string): boolean {
  if (!text) return false;
  const normalized = text.trim();
  if (!normalized) return false;
  const lower = normalized.toLowerCase();
  for (const word of CRISIS_WORDS) {
    if (word === 'OD') {
      // 「OD」は単独のトークンとして現れたときのみ危機ワードと判定する。
      // 単純な includes('od') だと good / food / mood / today などの一般英単語に
      // 誤反応して窓口案内が付いてしまうため、前後が英字でない（独立した od）場合のみ拾う。
      if (/(?<![a-z])od(?![a-z])/.test(lower)) return true;
      continue;
    }
    if (normalized.includes(word)) return true;
  }
  return false;
}

